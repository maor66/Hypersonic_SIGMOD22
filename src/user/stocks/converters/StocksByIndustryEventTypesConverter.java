	@Override
	public EventType convertToKnownEventType(String[] rawEvent) {
		String eventCompanyLabel = rawEvent[0];
		if (eventCompanyLabel.equals(megaLargeCompanyLabel)) {
			return StockEventTypesManager.megaLargeCompanyEventType;
		}
		for (int i = 0; i < financeCompaniesLabels.length; ++i) {
			if (eventCompanyLabel.equals(financeCompaniesLabels[i]))
				return StockEventTypesManager.financeCompanyEventType;
		}
		for (int i = 0; i < hiTechCompaniesLabels.length; ++i) {
			if (eventCompanyLabel.equals(hiTechCompaniesLabels[i]))
				return StockEventTypesManager.highTechCompanyEventType;
		}
		return null;
	}
	
